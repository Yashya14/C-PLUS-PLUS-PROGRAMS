// program of if else-if ladder statement 

#include<iostream>
using namespace std;

int main(){
    int i=20;

    if(i==10){
        cout<<"i is 10";
    }else if(i==15){
        cout<<"i is 15";
    }else if(i==20){
        cout<<"i is 20";
    }else{
        cout<<"i is incorrect";
    }
    
    return 0;
}