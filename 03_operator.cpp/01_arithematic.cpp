#include<iostream>
using namespace std;

int main(){
    // Arithematic operator in cpp
    int a = 10, b = 2;

    cout<<"The value of a + b is : "<<a+b<<endl;
    cout<<"The value of a - b is : "<<a-b<<endl;
    cout<<"The value of a * b is : "<<a*b<<endl;
    cout<<"The value of a / b is : "<<a/b<<endl;
    cout<<"The value of a % b is : "<<a%b<<endl;
    cout<<"The value of a++ is : "<<a++<<endl;
    cout<<"The value of ++a is : "<<++a<<endl;
    cout<<"The value of a-- is : "<<a-- <<endl;
    cout<<"The value of --a b is : "<< --a<<endl;
   
    return 0;
}

